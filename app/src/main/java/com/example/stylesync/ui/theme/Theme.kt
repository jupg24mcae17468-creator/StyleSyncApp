private val LightColorScheme = lightColorScheme(
    primary = StyleSyncPrimary,
    onPrimary = Color.White,
    secondary = StyleSyncSecondary,
    onSecondary = StyleSyncOnSurface,
    tertiary = StyleSyncPrimaryVariant,
    onTertiary = Color.White,
    background = StyleSyncBackground,
    onBackground = StyleSyncOnBackground,
    surface = Color.White,
    onSurface = StyleSyncOnSurface,
    surfaceVariant = StyleSyncBackground,
    onSurfaceVariant = StyleSyncOnSurface
)

private val DarkColorScheme = darkColorScheme(
    primary = StyleSyncDarkPrimary,
    onPrimary = Color.White,
    secondary = StyleSyncDarkAccent,
    onSecondary = Color.White,
    tertiary = StyleSyncDarkSurface,
    onTertiary = StyleSyncDarkBackground,
    background = StyleSyncDarkBackground,
    onBackground = Color.White,
    surface = StyleSyncDarkBackground,
    onSurface = Color.White,
    surfaceVariant = StyleSyncDarkAccent,
    onSurfaceVariant = StyleSyncDarkSurface
)

